package ex_04_Java_Operators;

public class Lab_0024_Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 40;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
